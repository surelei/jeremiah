package com.jeremiahxu.map.baidu;

/**
 * 坐标对象
 * 
 * @author Jeremiah Xu
 *
 */
public class Coords {
	private double lng;// 经度
	private double lat;// 纬度

	public double getLng() {
		return lng;
	}

	public void setLng(double lng) {
		this.lng = lng;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}
}
